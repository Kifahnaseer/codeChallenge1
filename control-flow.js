// switch 
let totalMarks= 350
let average =totalMarks / 5

switch (average) {
    case 20:
        console.log ("Grade D")
        break;
    case 40:
        console.log("Grade C");
        break;
    case 60:
        console.log("Grade C");
        break;
    case 80:
        console.log("Grade B");
        break;
    case 100:
        console.log("Grade A");
        break;
    default:
        break;
}

// 0 - 39 = Grade D
// 40 - 100 - Grade A